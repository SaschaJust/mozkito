package org.mozkito.mappings.model;

import java.util.HashSet;
import java.util.LinkedList;
import java.util.Set;

import javax.persistence.ElementCollection;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Transient;

import net.ownhero.dev.ioda.Tuple;
import net.ownhero.dev.kanuni.conditions.CompareCondition;
import net.ownhero.dev.kanuni.conditions.Condition;

import org.mozkito.mappings.finder.Finder;
import org.mozkito.mappings.mappable.model.MappableEntity;
import org.mozkito.mappings.selectors.Selector;
import org.mozkito.persistence.Annotated;

/**
 * This class represents a potential mapping pair and thus encapsulates to {@link MappableEntity}s. {@link Candidate}s
 * are generated by the {@link Finder} class using {@link Finder#getCandidates(MappableEntity, Class)}.
 * 
 * @author Sascha Just <sascha.just@mozkito.org>
 * 
 */
@Entity
public class Candidate implements Annotated {
	
	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 1464864624834219097L;
	
	/** the entity under subject. */
	private MappableEntity    from;
	
	/** The preselectors. */
	private Set<String>       selectors        = new HashSet<String>();
	
	/** a potential target. */
	private MappableEntity    to;
	
	/**
	 * Instantiates a new candidate.
	 */
	@Deprecated
	public Candidate() {
		// for OpenJPA only
	}
	
	/**
	 * Instantiates a new candidate.
	 * 
	 * @param candidatePair
	 *            a {@link Tuple} representing the pair (from/to)
	 * @param activeSelectors
	 *            the selectors
	 */
	public Candidate(final Tuple<? extends MappableEntity, ? extends MappableEntity> candidatePair) {
		this.from = candidatePair.getFirst();
		this.to = candidatePair.getSecond();
	}
	
	/*
	 * (non-Javadoc)
	 * @see org.mozkito.mappings.model.ICandidate#addSelector(org.mozkito.mappings.selectors.Selector)
	 */
	@Transient
	public boolean addSelector(final Selector selector) {
		return this.selectors.add(selector.getHandle());
	}
	
	/**
	 * @param selectors
	 */
	@Transient
	public void addSelectors(final Set<Selector> selectors) {
		// PRECONDITIONS
		
		try {
			for (final Selector selector : selectors) {
				addSelector(selector);
			}
		} finally {
			// POSTCONDITIONS
		}
	}
	
	@Override
	@Transient
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		final Candidate other = (Candidate) obj;
		if (this.from == null) {
			if (other.from != null) {
				return false;
			}
		} else if (!this.from.equals(other.from)) {
			return false;
		}
		if (this.to == null) {
			if (other.to != null) {
				return false;
			}
		} else if (!this.to.equals(other.to)) {
			return false;
		}
		return true;
	}
	
	/*
	 * (non-Javadoc)
	 * @see org.mozkito.mappings.model.ICandidate#getClass1()
	 */
	@Transient
	public String getClass1() {
		// PRECONDITIONS
		
		try {
			return getFrom().getBaseType().getCanonicalName();
		} finally {
			// POSTCONDITIONS
		}
	}
	
	/*
	 * (non-Javadoc)
	 * @see org.mozkito.mappings.model.ICandidate#getClass2()
	 */
	@Transient
	public String getClass2() {
		// PRECONDITIONS
		
		try {
			return getTo().getBaseType().getCanonicalName();
		} finally {
			// POSTCONDITIONS
		}
	}
	
	/*
	 * (non-Javadoc)
	 * @see org.mozkito.mappings.model.ICandidate#getFrom()
	 */
	@Id
	public final MappableEntity getFrom() {
		return this.from;
	}
	
	/**
	 * Gets the simple name of the class.
	 * 
	 * @return the simple name of the class.
	 */
	public final String getHandle() {
		// PRECONDITIONS
		
		final StringBuilder builder = new StringBuilder();
		
		try {
			final LinkedList<Class<?>> list = new LinkedList<Class<?>>();
			Class<?> clazz = getClass();
			list.add(clazz);
			
			while ((clazz = clazz.getEnclosingClass()) != null) {
				list.addFirst(clazz);
			}
			
			for (final Class<?> c : list) {
				if (builder.length() > 0) {
					builder.append('.');
				}
				
				builder.append(c.getSimpleName());
			}
			
			return builder.toString();
		} finally {
			// POSTCONDITIONS
			Condition.notNull(builder,
			                  "Local variable '%s' in '%s:%s'.", "builder", getClass().getSimpleName(), "getHandle()"); //$NON-NLS-1$ //$NON-NLS-2$ //$NON-NLS-3$
		}
	}
	
	/**
	 * @return the votingSelectors
	 */
	@ElementCollection
	public final Set<String> getSelectors() {
		// PRECONDITIONS
		
		try {
			return this.selectors;
		} finally {
			// POSTCONDITIONS
			Condition.notNull(this.selectors, "Field '%s' in '%s'.", "votingSelectors", //$NON-NLS-1$ //$NON-NLS-2$
			                  getClass().getSimpleName());
		}
	}
	
	/*
	 * (non-Javadoc)
	 * @see org.mozkito.mappings.model.ICandidate#getTo()
	 */
	@Id
	public final MappableEntity getTo() {
		return this.to;
	}
	
	/*
	 * (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	@Transient
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = (prime * result) + ((this.from == null)
		                                                ? 0
		                                                : this.from.hashCode());
		result = (prime * result) + ((this.to == null)
		                                              ? 0
		                                              : this.to.hashCode());
		return result;
	}
	
	/**
	 * @param from
	 *            the from to set
	 */
	public void setFrom(final MappableEntity from) {
		// PRECONDITIONS
		Condition.notNull(from, "Argument '%s' in '%s'.", "from", getClass().getSimpleName()); //$NON-NLS-1$ //$NON-NLS-2$
		
		try {
			this.from = from;
		} finally {
			// POSTCONDITIONS
			CompareCondition.equals(this.from, from,
			                        "After setting a value, the corresponding field has to hold the same value as used as a parameter within the setter."); //$NON-NLS-1$
		}
	}
	
	/**
	 * @param selectors
	 *            the selectors to set
	 */
	public void setSelectors(final Set<String> selectors) {
		// PRECONDITIONS
		Condition.notNull(selectors, "Argument '%s' in '%s'.", "selectors", getClass().getSimpleName()); //$NON-NLS-1$ //$NON-NLS-2$
		
		try {
			this.selectors = selectors;
		} finally {
			// POSTCONDITIONS
			CompareCondition.equals(this.selectors, selectors,
			                        "After setting a value, the corresponding field has to hold the same value as used as a parameter within the setter."); //$NON-NLS-1$
		}
	}
	
	/**
	 * @param to
	 *            the to to set
	 */
	public void setTo(final MappableEntity to) {
		// PRECONDITIONS
		Condition.notNull(to, "Argument '%s' in '%s'.", "to", getClass().getSimpleName()); //$NON-NLS-1$ //$NON-NLS-2$
		
		try {
			this.to = to;
		} finally {
			// POSTCONDITIONS
			CompareCondition.equals(this.to, to,
			                        "After setting a value, the corresponding field has to hold the same value as used as a parameter within the setter."); //$NON-NLS-1$
		}
	}
	
	/*
	 * (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		final StringBuilder builder = new StringBuilder();
		builder.append(getHandle());
		builder.append(" [from="); //$NON-NLS-1$
		builder.append(this.from);
		builder.append(", to="); //$NON-NLS-1$
		builder.append(this.to);
		builder.append("]"); //$NON-NLS-1$
		return builder.toString();
	}
}
